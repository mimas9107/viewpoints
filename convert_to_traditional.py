def convert_text(text):
    """將文字中的簡體轉為繁體"""
    for simp, trad in CONVERSIONS.items():
        text = text.replace(simp, trad)
    return text


def convert_file(filepath):
    """轉換單個檔案"""
    print(f"轉換: {filepath}")

    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    original = content

    # 對於不同類型的檔案進行不同的處理

    # 1. Markdown 檔案：全部轉換
    if filepath.endswith(".md"):
        content = convert_text(content)

    # 2. HTML 檔案：轉換註解和可見文字
    elif filepath.endswith(".html"):
        lines = content.split("\n")
        new_lines = []
        for line in lines:
            # 處理 HTML 註解
            if "<!--" in line:
                parts = line.split("<!--")
                new_parts = [parts[0]]
                for i, part in enumerate(parts[1:], 1):
                    if "-->" in part:
                        comment, after = part.split("-->", 1)
                        new_parts.append("<!--" + convert_text(comment) + "-->" + after)
                    else:
                        new_parts.append("<!--" + convert_text(part))
                line = "".join(new_parts)
            # 處理可見文字（在標籤之間的內容）
            else:
                line = convert_text(line)
            new_lines.append(line)
        content = "\n".join(new_lines)

    # 3. JavaScript/Python 檔案：只轉換註解和字串
    elif filepath.endswith((".js", ".py")):
        lines = content.split("\n")
        new_lines = []
        for line in lines:
            new_line = line

            # Python 註解
            if "#" in line:
                parts = line.split("#", 1)
                code_part = parts[0]
                comment_part = "#" + parts[1] if len(parts) > 1 else ""
                new_line = code_part + convert_text(comment_part)

            # JavaScript 註解
            elif "//" in line:
                parts = line.split("//", 1)
                code_part = parts[0]
                comment_part = "//" + parts[1] if len(parts) > 1 else ""
                new_line = code_part + convert_text(comment_part)

            # HTML 註解（在 JavaScript 中嵌入了 HTML）
            elif "<!--" in line:
                parts = line.split("<!--")
                new_parts = [parts[0]]
                for i, part in enumerate(parts[1:], 1):
                    if "-->" in part:
                        comment, after = part.split("-->", 1)
                        new_parts.append("<!--" + convert_text(comment) + "-->" + after)
                    else:
                        new_parts.append("<!--" + convert_text(part))
                new_line = "".join(new_parts)

            # 轉換字串中的中文（簡單處理）
            # 找到 "..." 或 '...' 中的中文
            def replace_string(match):
                quote = match.group(1)
                text = match.group(2)
                if any("\u4e00" <= c <= "\u9fff" for c in text):
                    text = convert_text(text)
                return quote + text + quote

            # 只轉換注釋後的部分（簡化處理）
            if "//" in new_line:
                idx = new_line.index("//")
                code = new_line[:idx]
                comment = new_line[idx:]
                # 不再處理 code 部分的中文，避免破壞程式碼
                new_line = code + convert_text(comment)

            new_lines.append(new_line)

        content = "\n".join(new_lines)

    # 寫回檔案
    if content != original:
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"  ✅ 已轉換: {filepath}")
        return True
    else:
        print(f"  ⏭️  無需轉換: {filepath}")
        return False


